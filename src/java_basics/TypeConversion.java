package java_basics;

 class TypeConversion {

	public static void main(String[] args) {
//Float to int
	float f= 11.23f;
	int a=(int)f;
	System.out.println(f);
	System.out.println(a);
//int to byte
	int b=140;
	byte c=(byte)b;
	System.out.println(b);
	System.out.println(c);
//Addition of byte
byte d= 10;
byte e= 20;
byte g=(byte)(d+e);
System.out.println(g);
	
	
	}

}
